/*
Entorno de Ejecución

- JavaScript se ejecuta en diferentes entornos, siendo los más comunes los navegadores web y Node.js.
- En los navegadores, JavaScript interactúa con el DOM (Document Object Model) para manipular elementos HTML y CSS.
- Los navegadores proporcionan un entorno seguro llamado "sandbox" que limita el acceso de JavaScript a ciertas funcionalidades del sistema operativo por razones de seguridad.
- Node.js es un entorno de ejecución de JavaScript del lado del servidor que permite ejecutar JavaScript fuera del navegador.
- Node.js utiliza el motor V8 de Google Chrome para interpretar y ejecutar código JavaScript.
- En Node.js, JavaScript puede interactuar con el sistema de archivos, redes y otros recursos del sistema operativo.
- Ambos entornos (navegadores y Node.js) tienen sus propias APIs específicas que permiten realizar tareas particulares según el contexto.
- Es importante entender las diferencias entre estos entornos para escribir código JavaScript efectivo y seguro.

- Buenas practicas: https://google.github.io/styleguide/jsguide.html

*/