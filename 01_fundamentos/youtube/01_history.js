/* 

Historia

- Creado en 1995.
- Se ideo como un lenguaje para la web.
- Fue desarrollado por Brendan Eich en 10 días.
- Originalmente se llamaba Mocha, luego LiveScript, y finalmente JavaScript.
- Javscript no está relacionado con Java, a pesar de la similitud en el nombre.
- Javascript es un lenguaje de programación interpretado, orientado a objetos y basado en prototipos.
- Javascript se ejecuta principalmente en navegadores web, pero también en servidores (Node.js).
- Es un lenguaje dinámico y débilmente tipado, esto significa que las variables pueden cambiar de tipo y no es necesario declarar el tipo de dato.
- En 1997, fue estandarizado como ECMAScript por ECMA International.
- Ha evolucionado significativamente con versiones como ES5 (2009), ES6 (2015) y posteriores.
- Hoy en día, es uno de los lenguajes de programación más populares del mundo, utilizado tanto en el frontend como en el backend.
- Javascript vanilla se refiere a usar JavaScript puro, sin bibliotecas o frameworks adicionales.


*/